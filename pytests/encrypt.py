#!/bin/python3
# Enter  a string in upper //convert all entries to uppercase
stris = input("Enter a keyword: ").upper

sec_message = []

#covert string to unicode ascii and add to the string
for guza in stris:
    ord(guza)
    print(guza, end = '')
    sec_message = sec_message.append()

# output the message in ascii
print("Message is: ", sec_message)

#create list for this
new = []

# convert back to message
for i in sec_message:
    new = chr(sec_message[i, i+1])
    new = new.append

# print it out
print("Your message was: ", new)
    